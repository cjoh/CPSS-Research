**Research Date:** January 5, 2025

## Legal Definition
**Status:** Certified Peer Recovery Specialist (CPRS)

**Definition:** "Not a sponsor, case manager or therapist but rather a role model, mentor, advocate, and motivator."

**Administered by:** Delaware Certification Board (DCB)

## Coaching vs. Therapy
Peer recovery specialists distinct from therapists. Clear boundaries established.

**Licensed professionals regulated under:** 24 Del. C., Chapter 30 (Board of Mental Health and Chemical Dependency Professionals)

## Independent Practice
**Status:** Certification required

**Requirements:**
- High school diploma/GED
- Direct personal experience with mental illness and/or addiction and recovery
- 46 hours training (10 Advocacy, 10 Mentoring/Education, 10 Recovery/Wellness, 16 Ethical Responsibility)
- 500 hours work/volunteer experience
- 1000-word essay on recovery experience
- Pass IC&RC Examination for Peer Recovery Specialists

**Independent practice:** Not explicitly addressed in available materials

**Citation:** Delaware Certification Board standards

## Summary
- **Legally defined?** YES - through certification program
- **Independent practice?** Not explicitly addressed
