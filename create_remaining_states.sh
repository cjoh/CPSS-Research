# Connecticut Recovery Coaching Legal Requirements
**Research Date:** January 5, 2025

## Legal Definition
**Two certifications:**
1. Recovery Support Specialist (RSS) - 80-hour training
2. Peer Recovery Coach (PRC) - CCAR 30-hour training

**Note:** PRC certificate of attendance is NOT certification; RSS is actual state certification.

**Administered by:** Connecticut Certification Board

## Coaching vs. Therapy
Recovery coaches provide peer support, not clinical treatment. Clear distinction from therapists.

## Independent Practice
**Status:** Certification required for employment

**RSS Requirements:**
- 80-hour training
- State certification exam
- 60 hours CE every 3 years (no fee to renew)

**CPRS (IC&RC) also available:**
- $50 annual renewal fee
- Must renew annually

**Important:** Not all state certifications transferable between states - check requirements.

**Citation:** Connecticut Certification Board standards

## Summary
- **Legally defined?** YES - through state certification
- **Independent practice?** Must verify Connecticut-specific requirements
- **Interventionists different?** No information found

**Contact:** Connecticut Certification Board: ctcertboard.org
